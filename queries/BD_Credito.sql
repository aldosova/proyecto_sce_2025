CREATE TABLE CLIENTE (
    ID_CLIENTE INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
    NOMBRE VARCHAR(100),
    EMAIL VARCHAR(100),
    TEL VARCHAR(20),
    STATUS VARCHAR(1), -- 'A' activo
    MONTO_MAX DECIMAL(10,2),
    MONTO_DISPONIBLE DECIMAL(10,2),
    MONTO_UTILIZADO DECIMAL(10,2)
);
