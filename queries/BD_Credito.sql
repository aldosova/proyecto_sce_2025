CREATE TABLE OPERACION (
    ID_OPERACION INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
    ID_CLIENTE INT,
    ID_PROVEEDOR INT,
    MONTO DECIMAL(10,2),
    STATUS_OP VARCHAR(1),
    FECHA_OP TIMESTAMP,
    AUTORIZACION INT,
    FOLIO INT
);

CREATE SEQUENCE SEC_FOLIO_OPER
    START WITH 1000
    INCREMENT BY 1;